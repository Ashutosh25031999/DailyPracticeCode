from itertools import product


class Solution:
    def letterCombinations(self, digits: str) -> List[str]:
        keyboard = {
            "2": "abc",
            "3": "def",
            "4": "ghi",
            "5": "jkl",
            "6": "mno",
            "7": "pqrs",
            "8": "tuv",
            "9": "wxyz",
        }
        if not digits:
            return []

        sets = [keyboard[digit] for digit in digits]
        combinations = ["".join(comb) for comb in product(*sets)]
        return combinations
